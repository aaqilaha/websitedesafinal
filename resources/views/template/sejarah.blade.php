            Keberadaan Situs Margayuda juga melahirkan tradisi yang masih dilestarikan 
            oleh masyarakat setempat. Setelah dipelihara, terdapat tradisi yang dilakukan 
            secara turun temurun yang dinamakan Tradisi Ngikis. Tradisi Ngikis merupakan tradisi
             membersihkan dan memperbaiki makam menjelang bulan Ramadhan. Tradisi ini sendiri 
             hanya ada di Desa Jajawar dan biasanya dilakukan sekali pada hari Senin atau Kamis 
             sebelum memasuki bulan Ramadhan. 
            </p>
            <p style="text-align:justify">
