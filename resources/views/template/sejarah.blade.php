            hari. Setelah berkumpul, masyarakat mulai membersihkan makam-makam yang ada di 
            Makam Dalem Situs Margayuda. Setelah kegiatan bersih makam selesai, masyarakat 
            berkumpul dan mulai berbagi cerita sejarah tentang leluhur desa ini dan dilanjutkan 
            dengan berdoa. Hal ini dilakukan untuk mengenang dan menghormati jasa leluhur Desa 
            Jajawar. Sebagai bentuk penyambutan bulan suci Ramadhan, mereka juga melakukan ramah 
            tamah dan berbagi makan bersama.
            </p>
